//Variables are simply containers that store values
//they follow this pattern:
var yourVariableName = 12;

//They can store all of the values we've seen
var name = "Rusty";
var secretNumber = 73;
var isAdorable = true;

console.log(name);
console.log(secretNumber);
console.log(isAdorable);

//Recall the stored value by calling the variable name
var name = "Rusty";
console.log("hello there " + name); //"hello there Rusty"

var num = 37;
console.log(num);
num + 3 + 10 //50
console.log(num);

//We can also update existing variables
var name = "Robert";
console.log(name);
name = "Bob";
console.log(name);